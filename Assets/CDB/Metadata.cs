    public class FeatureDataDictionary
    {
        public Dictionary<char, Category> Categories = new Dictionary<char, Category>();

        public string Subdirectory(string facc)
        {
            if (facc.Length != 5)
                throw new ArgumentException("FeatureDataDictionary.Subdirectory(): invalid FACC");
            Category category = Categories[facc[0]];
            Category.Subcategory subcategory = category.Subcategories[facc[1]];
            Category.Subcategory.Type type = subcategory.Types[int.Parse(facc.Substring(2))];
            return string.Format("{0}_{1}/{2}_{3}/{4:000}_{5}", category.Key, category.Name, subcategory.Key, subcategory.Name, type.Key, type.Name);
        }


        internal void AddCategory(char key, string name) => Categories.Add(key, new Category(key, name));

        public class Category
        {
            public readonly char Key;
            public readonly string Name;
            public Dictionary<char, Subcategory> Subcategories = new Dictionary<char, Subcategory>();

            internal Category(char key, string name) { Key = key; Name = name; }

            internal void AddSubcategory(char key, string name) => Subcategories.Add(key, new Subcategory(key, name));

            public class Subcategory
            {
                public readonly char Key;
                public readonly string Name;
                public Dictionary<int, Type> Types = new Dictionary<int, Type>();

                internal Subcategory(char key, string name) { Key = key; Name = name; }

                internal void AddType(int key, string name) => Types.Add(key, new Type(key, name));

                public class Type
                {
                    public readonly int Key;
                    public readonly string Name;

                    internal Type(int key, string name) { Key = key; Name = name; }
                }
            }
        }
    }


