        private void DumpFeatureDataDictionaryToCode()
        {
            string filename = System.IO.Path.Combine(Database.Path, "Metadata", "Feature_Data_Dictionary.cs");
            using (var writer = System.IO.File.AppendText(filename))
            {
                foreach (var category in FeatureDataDictionary.Categories)
                {
                    writer.WriteLine(string.Format("FeatureDataDictionary.AddCategory('{0}', \"{1}\");", category.Key, category.Value.Name));
                    foreach (var subcategory in category.Value.Subcategories)
                    {
                        writer.WriteLine(string.Format("FeatureDataDictionary.Categories['{0}'].AddSubcategory('{1}', \"{2}\");", category.Key, subcategory.Key, subcategory.Value.Name));
                        foreach (var type in subcategory.Value.Types)
                        {
                            writer.WriteLine(string.Format("FeatureDataDictionary.Categories['{0}'].Subcategories['{1}'].AddType({2}, \"{3}\");", category.Key, subcategory.Key, type.Key, type.Value.Name));
                        }
                    }
                }
            }
        }


        private void InitializeFeatureDataDictionaryFromXML()
        {
            string filename = System.IO.Path.Combine(Database.Path, "Metadata", "Feature_Data_Dictionary.xml");
            var xml = new XmlDocument();
            xml.Load(filename);
            foreach (XmlNode child in xml.DocumentElement.ChildNodes)
            {
                if (child.Name == "Category")
                    InitializeFeatureDataDictionaryFromXML_Category(child);
            }
        }

        private void InitializeFeatureDataDictionaryFromXML_Category(XmlNode node)
        {
            char code = node.Attributes["code"].Value[0];
            foreach (XmlNode child in node.ChildNodes)
            {
                if (child.Name == "Label")
                    FeatureDataDictionary.AddCategory(code, child.InnerText);
                if (child.Name == "Subcategory")
                    InitializeFeatureDataDictionaryFromXML_Subcategory(code, child);
            }
        }

        private void InitializeFeatureDataDictionaryFromXML_Subcategory(char category, XmlNode node)
        {
            char code = node.Attributes["code"].Value[0];
            foreach (XmlNode child in node.ChildNodes)
            {
                if (child.Name == "Label")
                    FeatureDataDictionary.Categories[category].AddSubcategory(code, child.InnerText);
                if (child.Name == "Feature_Type")
                    InitializeFeatureDataDictionaryFromXML_FeatureType(category, code, child);
            }
        }

        private void InitializeFeatureDataDictionaryFromXML_FeatureType(char category, char subcategory, XmlNode node)
        {
            int code = int.Parse(node.Attributes["code"].Value);
            foreach (XmlNode child in node.ChildNodes)
            {
                if (child.Name == "Label")
                    FeatureDataDictionary.Categories[category].Subcategories[subcategory].AddType(code, child.InnerText);
            }
        }


